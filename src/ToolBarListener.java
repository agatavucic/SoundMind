/**
 * Toolbar action listener.
 * The controller implements these methods and changes the views.
 */
public interface ToolBarListener {
    /**
     * Show survey.
     */
    public void showSurvey();

    /**
     * Show history.
     */
    public void showHistory();

    /**
     * Show favorite playlists.
     */
    public void showFavorites();

    /**
     * Show text log.
     */
    public void showLog();
}
